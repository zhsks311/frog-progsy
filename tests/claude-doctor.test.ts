function fakeFs(files: Record<string, string>, realpaths: Record<string, string> = {}): FileSystemProbe {
  return {
    existsSync: (path: string) => Object.prototype.hasOwnProperty.call(files, path),
    readFileSync: (path: string) => {
      if (!Object.prototype.hasOwnProperty.call(files, path)) throw new Error(`missing ${path}`);
      return files[path]!;
    },
    realpathSync: (path: string) => realpaths[path] ?? path,
  };
}

