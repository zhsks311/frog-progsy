function withGrantHome<T>(fn: (ctx: { home: string; config: FrogConfig }) => T): T {
  const prev = process.env.FROGPROGSY_HOME;
  const home = mkdtempSync(join(tmpdir(), "frogp-doctor-grants-"));
  process.env.FROGPROGSY_HOME = home;
  try {
    const config: FrogConfig = { port: 10100, defaultProvider: "codex", providers: {} };
    return fn({ home, config });
  } finally {
    if (prev === undefined) delete process.env.FROGPROGSY_HOME;
    else process.env.FROGPROGSY_HOME = prev;
    rmSync(home, { recursive: true, force: true });
  }
}

